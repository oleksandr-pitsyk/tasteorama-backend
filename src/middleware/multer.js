// ===================================================================================
// Middleware - Перевірка параметрів файлу
// ===================================================================================
import multer from 'multer';

export const upload = multer({
  storage: multer.memoryStorage(), // - зберігає файл у пам’яті сервера (не на диску)
  limits: {
    fileSize: 2 * 1024 * 1024, // - обмежує розмір завантаження до 2 МБ.
  },
  // fileFilter - це функція, яку multer викликає для кожного завантаженого файлу.
  // Вона отримує три аргументи:
  // req - HTTP-запит, як у звичайному Express;
  // file - інформація про файл (назва, MIME-тип, розмір тощо);
  // cb - callback, який повідомляє multer, що робити з файлом.
  // Варіанти виклику cb:
  // cb(null, true) - файл дозволено (приймаємо);
  // cb(null, false) - файл відхилено без помилки;
  // cb(new Error('...')) - файл відхилено з помилкою, обробка переривається.
  // У цьому коді:
  // якщо mimetype не співпадає з жодним із дозволених значень, cb з помилкою;
  // інакше - cb(null, true), тобто файл приймається.
  fileFilter: (req, file, cb) => {
    // визначає, які файли дозволено приймати.
    // ----------------------------------------------------------------------------------------
    // Варіант 1 - конспект :
    // У цьому випадку - лише ті, чий mimetype співпадає із можливими значеннями.
    // const allowedTypes = ['image/jpeg', 'image/jpg', 'image/png', 'image/gif', 'image/webp'];

    // if (allowedTypes.includes(file.mimetype)) {
    //   // cb(null, true) - файл дозволено (приймаємо);
    //   cb(null, true);
    // } else {
    //   // cb(new Error('...')) - файл відхилено з помилкою, обробка переривається.
    //   cb(new Error('Only images allowed'), false);
    // }
    // ----------------------------------------------------------------------------------------

    // ----------------------------------------------------------------------------------------
    // Варіант 2 - лекція та перевірка ДЗ ШІ :
    // ----------------------------------------------------------------------------------------
    if (!file.mimetype || !file.mimetype.startsWith('image/')) {
      //  cb(new Error('...')) - файл відхилено з помилкою, обробка переривається.
      cb(new Error('Only images allowed'), false);
      // Вихід з функції при помилці
      return;
    } else {
      // cb(null, true) - файл дозволено (приймаємо);
      cb(null, true);
    }
  },
});
